  library(nameGenerator)
  library(dplyr)

## ------------------------------------------------------------------------
  eras <- expand.grid(Year = seq(1880, 2010, by = 10),
                      Sex = c("M","F"))

## ------------------------------------------------------------------------
  eras %>%
    group_by(Year, Sex)%>%
    mutate(name = most_prob_name(Year = Year, Sex = Sex))

